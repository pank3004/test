/*
Abstraction: 

Abstraction is a very important and useful feature in OOPs which enables us to showcase or display only
essential information and hide the details related to that information. It is the process of exposing to the outside
world only the information that is absolutely necessary while obscuring implementation or background
information.

Consider the example of pow() function in C++ library math.h . When we use this function we just pass two
numbers (n,m) as arguments and the function returns us mth power of n. We use the function without knowing
how it is working internally so this is abstraction.
*/